function cleanTitle(title) {
  return title
    .replace(/^Case Study\s*[—:-]\s*/i, "")
    .replace(/\s*\|\s*Clickmasters$/i, "");
}

function normalizeValue(value) {
  return value
    ?.trim()
    .toLowerCase()
    .replace(/&/g, "and")
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/^-+|-+$/g, "");
}

function paragraphAfterHeading(content = [], heading) {
  const headingIndex = content.findIndex(
    (section) =>
      section.type === "heading" &&
      normalizeValue(section.text) === normalizeValue(heading)
  );

  if (headingIndex === -1) return undefined;

  return content
    .slice(headingIndex + 1)
    .find((section) => section.type === "paragraph")?.text;
}

function detectIndustry(item) {
  const text = `${item.title} ${item.excerpt} ${(item.credibility || []).join(" ")}`.toLowerCase();

  if (text.includes("defi") || text.includes("yield") || text.includes("lending")) return "DeFi";
  if (text.includes("real estate") || text.includes("escrow")) return "Real Estate";
  if (text.includes("retail") || text.includes("loyalty")) return "Retail";
  if (text.includes("financial") || text.includes("payment") || text.includes("payroll")) return "Financial Services";
  if (text.includes("supply chain") || text.includes("coffee") || text.includes("pharma")) return "Supply Chain";
  if (text.includes("clinical") || text.includes("healthcare") || text.includes("fda")) return "Healthcare";
  if (text.includes("gamefi") || text.includes("gaming")) return "GameFi";
  if (text.includes("government")) return "Government";
  if (text.includes("ticketing") || text.includes("festival")) return "Entertainment";
  if (text.includes("luxury")) return "Luxury Goods";
  if (text.includes("carbon")) return "Climate";

  return "Blockchain";
}

function detectService(item) {
  const text = `${item.title} ${item.excerpt} ${(item.credibility || []).join(" ")}`.toLowerCase();

  if (text.includes("tokenized") || text.includes("tokenization") || text.includes("carbon credit")) return "Tokenization";
  if (text.includes("hyperledger") || text.includes("procurement") || text.includes("supply chain") || text.includes("clinical")) return "Enterprise Blockchain";
  if (text.includes("escrow") || text.includes("erc-") || text.includes("contract")) return "Smart Contracts";
  if (text.includes("payment") || text.includes("payroll") || text.includes("settlement")) return "Payments";
  if (text.includes("nft")) return "NFT Development";

  return "Protocol Development";
}

function detectResult(item) {
  const candidates = [...(item.credibility || []), item.excerpt, item.hero?.description].filter(Boolean);
  const result = candidates.find((entry) =>
    /(\$[\d.]+[MBK]?|\d+(\.\d+)?\s?[%×x]|zero|resolved|minute|day|month)/i.test(entry)
  );

  if (!result) return ["Measured", "Project Result"];

  const metric = result.match(/\$[\d.]+[MBK]?|\d+(\.\d+)?\s?[%×x]|Zero|Resolved|\d+[- ]?minute|\d+[- ]?day|\d+[- ]?month/i)?.[0];

  return [metric || result, result.replace(metric || "", "").trim() || "Project Result"];
}

function detectDuration(item) {
  const timeline = paragraphAfterHeading(item.content, "Timeline:");
  const timelineMatch =
    timeline?.match(/\d+\s*(weeks?|months?|days?)/i)?.[0] ||
    item.excerpt?.match(/\d+\s*(weeks?|months?|days?)/i)?.[0];

  return timelineMatch || item.readTime || "Project";
}

